"use server";

import { redirect } from "next/navigation";
import { endSession } from "@/lib/admin-auth";

/*
  Выход из панели.

  Серверное действие, а не POST-роут в /api, каким это было раньше: у
  серверных действий Next сам сверяет заголовок Origin с адресом сайта, и
  чужая страница не может разлогинить владельца невидимой формой. У обычного
  роута такой проверки нет — её пришлось бы писать руками и не забывать.

  Кнопка работает и без JavaScript: форму с серверным действием браузер
  отправляет как обычную, ещё до гидратации страницы.

  Удаляется только кука этого браузера. Если сессия утекла или потерян
  ноутбук — нужно закрыть все выданные сразу: сменить ADMIN_SESSION_VERSION
  в окружении сервера (см. admin-auth.ts) и перезапустить сайт.
*/
export async function logoutAdmin(): Promise<void> {
  await endSession();
  redirect("/admin/login");
}
