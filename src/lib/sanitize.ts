/*
  Очистка того, что пришло снаружи.

  Главное про XSS в React: текст, вставленный значением — `{query}` — уже
  безопасен. React печатает его текстовым узлом, `<script>` внутри строки
  останется набором символов на экране и разметкой не станет. Отдельная
  «санитизация поискового запроса перед выводом» ничего не защищает и только
  портит запрос покупателю (его «Динамики 6.5"» превратится в «6.5&quot;»).

  Дыры возникают ровно в трёх местах, и все три закрыты здесь:
    1. dangerouslySetInnerHTML   → escapeHtml
    2. адрес в href/src          → safeHref  (javascript:… исполняется по клику)
    3. текст без ограничений      → cleanQuery (длина и управляющие символы)
*/

/* --------------------------------- адреса -------------------------------- */

/*
  Что можно пускать в href. `javascript:` и `data:` в этом списке нет
  намеренно: первый выполняет код по клику от имени нашего домена, второй
  открывает документ с чужой разметкой там же.
*/
const ALLOWED_SCHEMES = new Set(["http:", "https:", "mailto:", "tel:"]);

/**
 * Адрес, который не жалко подставить в href.
 *
 * Нужен там, где ссылку задаём не мы: реквизиты и контакты правятся из
 * панели, адрес оплаты приходит от платёжного сервиса. Непонятное значение
 * заменяется заглушкой, а не пропускается «как есть».
 */
export function safeHref(raw: string | null | undefined, fallback = "#"): string {
  const value = raw?.trim();
  if (!value) return fallback;

  // Относительные адреса — свои, проверять нечего. Кроме «//чужой.сайт»:
  // браузер читает это как внешнюю ссылку с текущей схемой.
  if (value.startsWith("/") && !value.startsWith("//")) return value;
  if (value.startsWith("#") || value.startsWith("?")) return value;

  try {
    // База нужна, чтобы конструктор не бросал на «example.com/page».
    const url = new URL(value, "https://momo-eq.ru");
    return ALLOWED_SCHEMES.has(url.protocol) ? url.toString() : fallback;
  } catch {
    return fallback;
  }
}

/* -------------------------------- разметка ------------------------------- */

const HTML_ENTITIES: Record<string, string> = {
  "&": "&amp;",
  "<": "&lt;",
  ">": "&gt;",
  '"': "&quot;",
  "'": "&#39;",
};

/**
 * Экранирование для тех редких мест, где строку приходится отдавать
 * разметкой (dangerouslySetInnerHTML, письма, ручная сборка HTML).
 *
 * Это не «умный» санитайзер: он не разбирает теги и не решает, какие из них
 * приличные, — он лишает символы их разметочного смысла целиком. Если
 * когда-нибудь понадобится оставить покупателю жирный шрифт и списки
 * (отзывы, описания из панели), нужен разборщик вроде DOMPurify: попытка
 * вырезать «плохие» теги регулярками обходится десятком известных способов.
 */
export function escapeHtml(value: string): string {
  return value.replace(/[&<>"']/g, (ch) => HTML_ENTITIES[ch]);
}

/* --------------------------------- запрос -------------------------------- */

// Ста символов хватает самому длинному названию в каталоге с запасом.
const MAX_QUERY = 100;

/**
 * Нормализация поискового запроса.
 *
 * Не про XSS (его закрывает React), а про то, чтобы запрос не был оружием:
 * мегабайтная строка из адреса уезжает в разметку, в индекс, в Метрику и в
 * регулярное выражение поиска. Плюс убираем управляющие символы и склейку
 * пробелов — они ничего не находят, зато ломают подсветку и заголовки.
 */
export function cleanQuery(raw: string | null | undefined): string {
  if (!raw) return "";
  return raw
    .replace(/[\u0000-\u001F\u007F]/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, MAX_QUERY);
}
