import { headers } from "next/headers";

/*
  Одноразовая метка запроса для встроенных скриптов.

  Её выдаёт proxy.ts на каждый запрос и он же перечисляет в политике
  безопасности (script-src 'nonce-…'). Скрипт без метки браузер не выполнит —
  включая тот, что попал в страницу через дыру в наших данных.

  Свои служебные скрипты Next помечает сам, увидев политику в заголовках
  запроса. Руками метку нужно ставить только там, где мы пишем <script>
  собственными руками: разметка Schema.org и загрузчик Метрики.
*/
export async function requestNonce(): Promise<string | undefined> {
  return (await headers()).get("x-nonce") ?? undefined;
}
